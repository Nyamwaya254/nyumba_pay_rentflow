# landlord
class CreateLandlordRequest(BaseModel):
    """Super-admin payload to onbord a new landlord"""

    # user credentials
    email: EmailStr
    # password: str = Field(
    #     min_length=12,
    #     description="Min 12 chars — generated by super-admin, shared securely",
    # )
    # landlord profile
    full_name: str = Field(min_length=2, max_length=255)
    phone: str = Field(min_length=10, max_length=20, pattern=r"^\+?[\d\s\-]+$")
    business_name: str = Field(min_length=2, max_length=255)

    # daraja
    paybill_number: str = Field(
        min_length=5,
        max_length=20,
        pattern=r"^\d+$",
        description="Safaricom Paybill short code — digits only",
    )
    daraja_consumer_key: str = Field(
        min_length=10, description="From Safaricom Daraja app"
    )
    daraja_consumer_secret: str = Field(
        min_length=10, description="From Safaricom Daraja app"
    )
    daraja_environment: str = Field(
        default="production",
        pattern=r"^(production|sandbox)$",
    )

    @field_validator("phone")
    @classmethod
    def normalise_phone(cls, v: str) -> str:
        """Strip spaces and dashes.Normalise 07XX to 2547XX"""
        v = v.strip().replace(" ", "").replace("-", "").replace("+", "")
        if v.startswith("0") and len(v) == 10:
            v = "254" + v[1:]
        return v


