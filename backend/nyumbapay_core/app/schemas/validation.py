# unit
class CreateUnitRequest(BaseModel):
    unit_number: str = Field(min_length=1, max_length=20)
    floor: int | None = Field(None, ge=0)
    rent_amount: Decimal = Field(gt=0)

    @field_validator("unit_number")
    @classmethod
    def strip_unit_number(cls, v: str) -> str:
        v = v.strip()
        if not v:
            raise ValueError("unit_number cannot be blank")
        return v.upper()  # normalize to uppercase to match account_reference format


