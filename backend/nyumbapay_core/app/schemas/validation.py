class UpdateLandlordRequest(BaseModel):
    full_name: str | None = Field(None, min_length=2, max_length=255)
    phone: str | None = Field(None, min_length=10, max_length=20)
    business_name: str | None = Field(None, min_length=2, max_length=255)
    is_active: bool | None = None

    @model_validator(mode="after")
    def at_least_one_field(self) -> "UpdateLandlordRequest":
        values = [self.full_name, self.phone, self.business_name, self.is_active]
        if all(v is None for v in values):
            raise ValueError("At least one field must be provided")
        return self


