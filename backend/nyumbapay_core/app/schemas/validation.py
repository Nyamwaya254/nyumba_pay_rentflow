# water readings


class CreateWaterReadingsRequest(BaseModel):
    current_reading: Decimal = Field(ge=0)
    period: str = Field(
        pattern=r"^\d{4}-\d{2}$",
        description="YYYY-MM e.g. 2025-01",
    )

    @field_validator("period")
    @classmethod
    def validate_period(cls, v: str) -> str:
        year, month = v.split("-")
        if not (1 <= int(month) <= 12):
            raise ValueError("period month must be between 01 and 12")
        return v


